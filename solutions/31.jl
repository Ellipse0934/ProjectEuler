function solve()
    result = 0
    for a in 0:1
    for b in 0:2
    for c in 0:4
    for d in 0:10
    for e in 0:20
    for f in 0:40
    for g in 0:100
        if a * 200 +
            b * 100 +
            c * 50 +
            d * 20 +
            e * 10 +
            f * 5 +
            g * 2 <= 200
                result += 1
        end
    end
    end
    end
    end
    end
    end
    end
    result
end
